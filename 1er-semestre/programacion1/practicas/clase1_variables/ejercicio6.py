num1 = int(input("Ingrese el 1er número: "))
num2 = int(input("Ingrese el 2do número: "))
num3 = int(input("Ingrese el 3er número: "))
num4 = int(input("Ingrese el 4to número: "))
suma = num1 + num2 + num3 + num4
promedio = suma / 4
print(f'La suma de todos los números ingresados es: {num1} + {num2} + {num3} + {num4} = {suma} y el promedio es: {promedio}')